reboot:
      ;------------------------------------------------------------
      ; output message
      ;------------------------------------------------------------
      cdecl puts, .s0              ; // output reboot message

      ;------------------------------------------------------------
      ; waiting key input
      ;------------------------------------------------------------
.10L:
      
      mov ah, 0x10                 ;do
      int 0x16                     ;{
                                   ; // waiting key input
      cmp al, ' '                  ; AL = BIOS(0x16, 0x10);
      jne .10L                     ;} while(!ZF);

      ;------------------------------------------------------------
      ; output  LFCR
      ;------------------------------------------------------------
      cdecl puts, .s1             ;

      ;------------------------------------------------------------
      ; reboot
      ;------------------------------------------------------------
      int 0x19

      ;------------------------------------------------------------
      ; literal
      ;------------------------------------------------------------
.s0   db 0x0A, 0x0D, "Push SPACE key to reboot...", 0
.s1   db 0x0A, 0x0D, 0x0A, 0x0D, 0
