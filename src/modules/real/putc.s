putc:
  ;-----------------------------------------------
  ; construct stackframe
  ;-----------------------------------------------

  push bp
  mov  bp, sp

  ;-----------------------------------------------
  ; save  Register
  ;-----------------------------------------------
  push ax
  push bx

  ;-----------------------------------------------
  ; begining of process
  ;-----------------------------------------------
  mov al, [bp + 4]        ; get output char
  mov ah, [0x0E]          ; a teltype char output 
  mov bx, 0x0000          ; set 0  page number end char color
  int 0x10

  ;-----------------------------------------------
  ; return of register
  ;-----------------------------------------------

  pop bx
  pop ax

  ;-----------------------------------------------
  ; deconstruct stackframe
  ;-----------------------------------------------

  mov sp, bp
  pop bp

  ret
