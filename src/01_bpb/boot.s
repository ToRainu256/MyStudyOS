;**********************************************
; Entry Point
;**********************************************

entry:
  jmp ipl    ; jump to ipl

  ;--------------------------------------------------
  ; BPB (BIOS Parameter Block)
  ;--------------------------------------------------

  times 90 - ($ - $$) db 0x90

  ;--------------------------------------------------
  ; IPL(Initial Program Loader)
  ;--------------------------------------------------

ipl:
  ;--------------------------------------------------
  ; End of process
  ;--------------------------------------------------

  jmp $           ;while(true)
  
;**********************************************
; Boot Flag(End of Head 512 bytes)
;**********************************************
  times 510 - ($ - $$) db 0x00
  db 0x55, 0xAA



